var connection = require("./connection.js");


//prints question marks to an array to help generate MYSQL syntax
function printsQMarks(num) {
    var arr = [];

    for (var i = 0; i < num; i++) {
        arr.push("?");
    }

    return arr.toString();
}

//helps create mysql syntax from the key/value pairs
function objToSql(obj) {
    var arr = [];

    for (var key in obj) {
        arr.push(key + "=" + obj[key]);
    }

    return arr.toString();
}

